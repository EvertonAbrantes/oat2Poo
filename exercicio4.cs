class oat2
{
    static void Main(string[] args)
    {
        int a, a, c;

        Console.WriteLine("Digite o valor de A: ");
        a = int.Parse(Console.ReadLine());

        Console.WriteLine("Digite o valor de B: ");
        b = int.Parse(Console.ReadLine());

        if (a != b)
        {
            c = c * b;
        }
        else
        {
            c = a + b;
        }

        Console.WriteLine("O valor de C é: " + c);
    }
}
